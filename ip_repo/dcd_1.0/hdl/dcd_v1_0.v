		output wire  m00_axi_rready,
		
				// Ports of Axi Master Bus Interface M00_AXIS
        input wire  m00_axis_aclk,
        input wire  m00_axis_aresetn,
        output wire  m00_axis_tvalid,
        output wire [31 : 0] m00_axis_tdata,
        output wire [3 : 0] m00_axis_tstrb,
        output wire  m00_axis_tlast,
        input wire  m00_axis_tready
	);
	reg [31:0] tdata = 0;
	reg tvalid  = 0;
	reg tlast = 0;
	reg tlast_delay = 0;
	wire tready;
	
	assign m00_axis_tdata = tdata;
	assign m00_axis_tvalid = tvalid;
	assign m00_axis_tstrb = 4'b1111;
	assign m00_axis_tlast = tlast;
	assign tready = m00_axis_tready;
	
	
	always @ (posedge m00_axis_aclk) begin
	   tlast_delay <= tlast;
	   if (!m00_axis_aresetn) begin
	       tdata <= 0;
	       tvalid <= 0;
	       tlast <= 0;
	   end else begin
	       if (tready) begin
                if (tlast) begin
                   tvalid <= 0;
                   tdata <= 0;
                end else begin
                   tvalid <= 1;
                end
                
                if (tvalid) begin
                    tdata <= tdata + 1;
                end
                
                if (tdata == 7) begin
                    tlast <= 1;
                end
                if (tlast_delay == 1) begin
                    tlast <= 0;
                end
	       end else begin
                tdata <= 0;
                tvalid <= 0;
                tlast <= 0;
	       end
	   end
	end
	
